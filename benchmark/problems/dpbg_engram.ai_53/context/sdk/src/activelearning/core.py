"""
Core types and contracts for ActiveLearningAI.

These dataclasses define the fundamental data structures used throughout
the system for observations, actions, decisions, and beliefs.
"""

from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Generic, TypeVar, Optional
import time
import uuid


T = TypeVar("T")


class KernelDecisionType(Enum):
    """
    Decision types returned by the Moral Kernel.

    ALLOW: Approved as-is, proceed to execution
    TRANSFORM: Approved with modifications, apply transforms and proceed
    DENY: Rejected, do not execute
    DEFER: Needs human review, route to Dashboard for approval
    """
    ALLOW = "ALLOW"
    TRANSFORM = "TRANSFORM"
    DENY = "DENY"
    DEFER = "DEFER"


class BeliefNodeType(Enum):
    """Types of nodes in the Belief Graph."""
    VALUE = "value"      # Core values (e.g., "safety is important")
    NORM = "norm"        # Behavioral norms (e.g., "don't harm humans")
    FACT = "fact"        # Factual beliefs (e.g., "water is wet")


class BeliefEdgeType(Enum):
    """Types of edges in the Belief Graph."""
    SUPPORTS = "supports"       # Evidence for belief
    CONTRADICTS = "contradicts" # Evidence against belief
    ENTAILS = "entails"         # Logical implication
    REFINES = "refines"         # More specific version


@dataclass
class RiskAnalysis:
    """
    Risk analysis result from Safety Supervisor.

    Used by the Safety Supervisor to communicate risk assessments
    to the Moral Kernel for decision making.
    """
    trace_id: str
    risk_score: float = 0.0
    flags: list[str] = field(default_factory=list)
    recommendations: list[str] = field(default_factory=list)
    details: dict[str, Any] = field(default_factory=dict)


def generate_trace_id() -> str:
    """Generate a unique trace ID for tracking operations across components."""
    return str(uuid.uuid4())


def current_timestamp() -> int:
    """Get current Unix timestamp in milliseconds."""
    return int(time.time() * 1000)


@dataclass
class Observation(Generic[T]):
    """
    An observation from a sensor or external source.

    Observations flow through the system and are processed by the Planner
    to generate ActionProposals.

    Attributes:
        trace_id: Unique identifier for tracing this observation through the system
        provenance: Source of the observation (e.g., "sensor.camera.front")
        data: The actual observation payload (generic type)
        timestamp: Unix timestamp in milliseconds when observation was created
        confidence: Confidence score from 0.0 to 1.0
        tags: Semantic tags for categorization
    """
    trace_id: str
    provenance: str
    data: T
    timestamp: int = field(default_factory=current_timestamp)
    confidence: float = 1.0
    tags: list[str] = field(default_factory=list)

    def __post_init__(self) -> None:
        if not self.trace_id:
            self.trace_id = generate_trace_id()
        if self.confidence < 0.0 or self.confidence > 1.0:
            raise ValueError(f"Confidence must be between 0.0 and 1.0, got {self.confidence}")


@dataclass
class ActionProposal(Generic[T]):
    """
    A proposed action to be evaluated by the Moral Kernel.

    ActionProposals are generated by the Planner and must be approved
    by the Kernel before execution.

    Attributes:
        trace_id: Unique identifier linking to originating observation
        provenance: Source of the proposal (e.g., "planner.main")
        action: The proposed action payload (generic type)
        priority: Priority level (higher = more urgent)
        requires_approval: Whether human approval is needed
        metadata: Additional context for the Kernel
    """
    trace_id: str
    provenance: str
    action: T
    priority: int = 0
    requires_approval: bool = False
    metadata: dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        if not self.trace_id:
            self.trace_id = generate_trace_id()


@dataclass
class KernelDecision(Generic[T]):
    """
    A decision from the Moral Kernel about an ActionProposal.

    Attributes:
        trace_id: Links back to the original proposal
        type: The decision type (ALLOW, TRANSFORM, DENY, DEFER)
        reason: Human-readable explanation of the decision
        transformations: Modified actions if type is TRANSFORM
        risk_score: Computed risk score from 0.0 to 1.0
        expires_at: Unix timestamp when this decision expires
        issued_at: Unix timestamp when decision was made
    """
    trace_id: str
    type: KernelDecisionType
    reason: Optional[str] = None
    transformations: Optional[list[ActionProposal[T]]] = None
    risk_score: float = 0.0
    expires_at: Optional[int] = None
    issued_at: int = field(default_factory=current_timestamp)

    def is_expired(self) -> bool:
        """Check if this decision has expired."""
        if self.expires_at is None:
            return False
        return current_timestamp() > self.expires_at

    def remaining_ttl_ms(self, now: Optional[int] = None) -> Optional[int]:
        """Milliseconds left before this decision expires.

        Returns ``None`` when the decision has no expiry (it never goes
        stale), and ``0`` once it has already expired rather than a negative
        value — so callers can use the result directly as a non-negative
        cache TTL or freshness countdown.

        Args:
            now: Reference time in Unix milliseconds. Defaults to the current
                time; pass an explicit value for deterministic checks/tests.
        """
        if self.expires_at is None:
            return None
        reference = current_timestamp() if now is None else now
        return max(0, self.expires_at - reference)

    def is_approved(self) -> bool:
        """Check if this decision allows execution."""
        return self.type in (KernelDecisionType.ALLOW, KernelDecisionType.TRANSFORM)


@dataclass
class Outcome(Generic[T]):
    """
    The result of executing an action after Kernel approval.

    Attributes:
        trace_id: Links back to the original proposal/decision
        decision: The Kernel decision that authorized this outcome
        original: The original proposed action
        final: The final action that was executed (may be transformed)
        success: Whether execution succeeded
        error: Error message if execution failed
        timestamp: When the outcome was recorded
    """
    trace_id: str
    decision: KernelDecision[T]
    original: ActionProposal[T]
    final: Optional[ActionProposal[T]] = None
    success: bool = True
    error: Optional[str] = None
    timestamp: int = field(default_factory=current_timestamp)


@dataclass
class BeliefNode:
    """
    A node in the Belief Graph representing a belief or value.

    Attributes:
        id: Unique identifier for this belief
        type: Type of belief (value, norm, or fact)
        content: The belief content as text
        confidence: Confidence in this belief from 0.0 to 1.0
        source: Where this belief came from
        created_at: When this belief was created
        updated_at: When this belief was last updated
        metadata: Additional attributes
    """
    id: str
    type: BeliefNodeType
    content: str
    confidence: float = 1.0
    source: str = "unknown"
    created_at: int = field(default_factory=current_timestamp)
    updated_at: int = field(default_factory=current_timestamp)
    metadata: dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        if not self.id:
            self.id = generate_trace_id()
        if self.confidence < 0.0 or self.confidence > 1.0:
            raise ValueError(f"Confidence must be between 0.0 and 1.0, got {self.confidence}")


@dataclass
class BeliefEdge:
    """
    An edge in the Belief Graph representing a relationship between beliefs.

    Attributes:
        id: Unique identifier for this edge
        type: Type of relationship
        source_id: ID of the source belief node
        target_id: ID of the target belief node
        strength: Strength of the relationship from 0.0 to 1.0
        evidence: Supporting evidence for this relationship
        created_at: When this edge was created
    """
    id: str
    type: BeliefEdgeType
    source_id: str
    target_id: str
    strength: float = 1.0
    evidence: Optional[str] = None
    created_at: int = field(default_factory=current_timestamp)

    def __post_init__(self) -> None:
        if not self.id:
            self.id = generate_trace_id()
        if self.strength < 0.0 or self.strength > 1.0:
            raise ValueError(f"Strength must be between 0.0 and 1.0, got {self.strength}")


@dataclass
class AuditEntry:
    """
    An entry in the unified audit trail.

    All significant actions are logged here for traceability and debugging.

    Attributes:
        id: Unique identifier for this entry
        trace_id: Links to the operation being audited
        timestamp: When this entry was created
        component: Which component generated this entry
        action: What action was taken
        details: Additional context as JSON-serializable dict
        code_hash: SHA-256 hash of any code involved
    """
    id: str
    trace_id: str
    timestamp: int
    component: str
    action: str
    details: dict[str, Any] = field(default_factory=dict)
    code_hash: Optional[str] = None

    def __post_init__(self) -> None:
        if not self.id:
            self.id = generate_trace_id()
        if not self.timestamp:
            self.timestamp = current_timestamp()


@dataclass
class KnowledgeGap:
    """
    Represents a detected gap in the system's knowledge.

    When the system encounters a situation it cannot handle with existing
    knowledge, a KnowledgeGap is created and sent to the Meta-Programmer.

    Attributes:
        trace_id: Unique identifier for tracking
        description: Human-readable description of the gap
        context: Additional context about the situation
        search_results: What was found (if any) with low confidence
        confidence: Highest match confidence found
        available_sensors: Sensors available for learning
        allows_external_query: Whether external API queries are allowed
        source: What triggered the gap
    """
    trace_id: str
    description: str
    context: dict[str, Any] = field(default_factory=dict)
    search_results: list[dict[str, Any]] = field(default_factory=list)
    confidence: float = 0.0
    available_sensors: list[str] = field(default_factory=list)
    allows_external_query: bool = True
    source: str = "unknown"

    def __post_init__(self) -> None:
        if not self.trace_id:
            self.trace_id = generate_trace_id()


@dataclass
class CodeProposal:
    """
    A code change proposal from the Meta-Programmer to the Kernel.

    Attributes:
        trace_id: Unique identifier
        gap_ref: Reference to the KnowledgeGap being addressed
        proposed_action: CREATE, MODIFY, REFACTOR, or DELETE
        target_path: File path being modified
        code_preview: Preview of the proposed code
        test_plan: Plan for testing the code
        rollback_plan: Plan for reverting if needed
        agent: Which Meta-Programmer agent proposed this
    """
    trace_id: str
    gap_ref: Optional[str]
    proposed_action: str
    target_path: str
    code_preview: str
    test_plan: str = ""
    rollback_plan: str = ""
    agent: str = "unknown"

    def __post_init__(self) -> None:
        if not self.trace_id:
            self.trace_id = generate_trace_id()
        if self.proposed_action not in ("CREATE", "MODIFY", "REFACTOR", "DELETE"):
            raise ValueError(f"Invalid proposed_action: {self.proposed_action}")
